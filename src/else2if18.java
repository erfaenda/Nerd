public class else2if18 {
    public static void main(String[] args) {
        int Gomer;
        Gomer = 110;
        if (Gomer > 115) {
            System.out.println("Идиальный весь!!!");
        }
        else if (Gomer > 111){
            System.out.println("Супер идиален вес!");
        }
        else if (Gomer < 118){
            System.out.println("вес почти идален!");
        }
        else {
            System.out.println("Иди еще поешь");
        }
    }
}
